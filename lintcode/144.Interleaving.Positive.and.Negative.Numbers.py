class Solution:
    """
    @param a: An integer array.
    @return: nothing
    """
    def rerange(self, a: List[int]):
        # write your code here
        w, r = 0, 0
        pos_count, neg_count = 0, 0
        for num in a:
            if num >= 0:
                pos_count += 1
            else:
                neg_count += 1

        if pos_count >= neg_count:
            find_positive = 1
        else:
            find_positive = -1
        while w < len(a) and r < len(a):
            if find_positive * a[r] >= 0:
                a[w], a[r] = a[r], a[w]
                w += 1
                r = w
                find_positive = -find_positive
            else: 
                r += 1
        
            


